import java.util.Random;

public class Trap {
    private int trapX;
    private int trapY;

    public Trap(int trapX, int trapY) {
        this.trapX = trapX;
        this.trapY = trapY;
    }

    public int getTrapX() {
        return trapX;
    }

    public void setTrapX(int trapX) {
        this.trapX = trapX;
    }

    public int getTrapY() {
        return trapY;
    }

    public void setTrapY(int trapY) {
        this.trapY = trapY;
    }






}
